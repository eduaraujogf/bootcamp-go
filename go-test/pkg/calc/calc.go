package calc

// Sum receive two int numbers as a param and return the sum of both.
func Sum(a, b int) int {
	return a + b
}

// Sub receive two int numbers as a param and return the sub of both.
func Sub(a, b int) int {
	return a - b
}

// Mult receive two int numbers as a param and return the Mult of both.
func Multiply(a, b int) int {
	return a * b
}

// Division receive two int numbers as a param and return the Division of both.
func Division(a, b int) int {
	return a / b
}
